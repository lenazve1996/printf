#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int g;

	g = 0;
	while (src[g] != '\0')
	{
		dest[g] = src[g];
		++g;
	}
	dest[g] = src[g];
	return (dest);
}
